package exercicios;

public class diferenca {

	public static void main(String[] args) {
		int A, B, C, D;
		int resu;
		A = 5;
		B = 6;
		C = 7;
		D = 8;
		resu = A * B - C * D;
		System.out.println("DIFERENÇA = " + resu);
		
		int E, F, G, H;
		int result;
		E = 5;
		F = 6;
		G = -7;
		H = 8;
		result = E * F - G * H;
		System.out.println("DIFERENÇA = " + result);

	}

}
