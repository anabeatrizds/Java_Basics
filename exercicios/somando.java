public class somando {

	public static void main(String[] args) {
		int a, b;
		a = 10;
		b = 30;
		int soma;
		soma = a + b;
		System.out.println(soma);
		
		int d, e;
		d = -30;
		e = 10;
		int soma2;
		soma2 = d + e;
		System.out.println(soma2);
		
		int f, g;
		f = 0;
		g = 0;
		int soma3;
		soma3 = f + g;
		System.out.println(soma3);

	}

}
